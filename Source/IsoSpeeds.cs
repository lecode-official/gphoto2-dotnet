
namespace System.Devices
{
    /// <summary>
    /// Represents a container for some well-known ISO speeds.
    /// </summary>
    public static class IsoSpeeds
    {
        #region Public Constants
        
        /// <summary>
        /// Represents the automatic ISO speed, which means that the camera is determining the correct ISO speed based on the lighting conditions.
        /// </summary>
        public const int Auto = 0;
        
        /// <summary>
        /// Represents an ISO speed of 25.
        /// </summary>
        public const int Iso25 = 25;
        
        /// <summary>
        /// Represents an ISO speed of 50.
        /// </summary>
        public const int Iso50 = 50;
        
        /// <summary>
        /// Represents an ISO speed of 100.
        /// </summary>
        public const int Iso100 = 100;
        
        /// <summary>
        /// Represents an ISO speed of 125.
        /// </summary>
        public const int Iso125 = 125;
        
        /// <summary>
        /// Represents an ISO speed of 200.
        /// </summary>
        public const int Iso200 = 200;
        
        /// <summary>
        /// Represents an ISO speed of 400.
        /// </summary>
        public const int Iso400 = 400;
        
        /// <summary>
        /// Represents an ISO speed of 800.
        /// </summary>
        public const int Iso800 = 800;
        
        /// <summary>
        /// Represents an ISO speed of 1600.
        /// </summary>
        public const int Iso1600 = 1600;
        
        /// <summary>
        /// Represents an ISO speed of 3200.
        /// </summary>
        public const int Iso3200 = 3200;
        
        /// <summary>
        /// Represents an ISO speed of 6400.
        /// </summary>
        public const int Iso6400 = 6400;
        
        /// <summary>
        /// Represents an ISO speed of 12500.
        /// </summary>
        public const int Iso12500 = 12500;
        
        #endregion
    }
}